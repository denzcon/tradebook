				<h1>About <span class="hiliteTBBlue">trade</span><span class="hiliteTBGray">book</span></h1>
				<p><img src="../images/tradebook-vimeo.png" alt="tradebook video tour" /></p>
				<h2>We started tradebook in 2006 in Berlin, motivated by two ideas.</h2>
				<ul>
					<li>1. Provide fair, challenging opportunities for creative people, particularly those starting out, to earn money, learn, get their work seen and build their reputations.</li>
					<li>2. Enable organizations to create better communications, products and services by taking advantage of technologies to access talent spread around the world.</li>
				</ul>
				<p>In the following years, we pioneered a new way to do creative work online, rewarding not just the best ideas, but feedback and evaluation too. Along the way we discovered that this style of work has two other important benefits: new insights about ideas and creative talent as well as new communication value when the creative process is opened up.</p>
				<p>Our creative technology platform and community organizing processes, enable our unique <a href="http://www.tradebook.com/clients/how-we-work/">collaborative approach</a> yielding valuable ideas, insights and communications. It is what makes tradebook unique enabling us to work with talented creatives to take on the most challenging creative briefs. And we are fortunate to serve a global creative community as well as clients around the world including brands, agencies, governments and online communities.</p>
				<p>Today we’re also lucky to have the support of a private group of investors from the United States, United Kingdom and European Union to help us build a stable and sustainable business so that our clients, partners and community can rely on us. &nbsp;We’re looking for great clients to challenge and inspire our creative community. Please <a href="http://www.tradebook.com/clients/contact-us/">contact us</a>, if you are interested in being one of them.</p>
				<p>If you want to work on creative challenges, develop your skills and work with creatives from around the world, you’ve come to the right place &ndash; we hope you will <a href="http://www.tradebook.com/signup?contest=aboutpage&amp;remark=website">sign up</a> and take a look around.</p>
				<p>Regards,</p>
				<p>Bastian Unterberg (Co-founder &amp; CEO)</p>
